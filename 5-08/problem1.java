//  1)Write a java program to print 1 to 100 without using any numerical value ?

public class problem1{
    public static void main(String[] args) {
        char ch= 'd'/'d';   //100/100=1
        char ch1='d';      // ascii value  of d is 100
        for(int i=ch;i<=ch1;i++){
            System.out.print(i+" ");
        }
    }
}